function countTrees(stepsInput) {
  const array = data.split(/[\n\r]/gm);
  let position = { x: 0, y: 0 };
  let steps = stepsInput;
  let count = 0;
  // i a sor
  for (let i = 0; i <= array.length; ) {
    let sor = array[i];
    if (sor === undefined) {
      return count;
    }
    if (sor.charAt(position.x) === "#") {
      count++;
    }
    i += steps.y;
    if (i > array.length) {
      return count;
    }
    position.y = i;
    position.x = position.x + steps.x;
    for (let j = 0; position.x >= sor.length; ) {
      position.x = position.x - sor.length;
    }
  }
}
